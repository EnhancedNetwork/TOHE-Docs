<summary><b><font color=gray>Templates Info</font></b></summary>
<p>Open the root directory of the mod and find the <code v-pre>..\Among Us\TOHE-DATA\template.txt</code> file.</p>
<p>You can see that there are some words in the file, such as <code v-pre>welcome</code>, <code v-pre>onMeeting</code>, and other gibberish.</p>
<ul>
<li><code v-pre>welcome:exampleMessage</code> - this is the message that will be sent when other players enter your lobby.</li>
<li><code v-pre>onMeeting:exampleMessage</code> - this is the message that will be sent when each meeting starts.</li>
<li><code v-pre>onFirstMeeting:exampleMessage</code> - this is just like <code v-pre>onMeeting</code>, but the message sent here will only be sent on the <i>first</i> meeting. Any meetings after will prioritize <code v-pre>onMeeting</code>.</li>
</ul>
<p>You can edit these templates, or add your own on new-lines!</p>
<p>On a new-line, you want to add your trigger word and your message. Such as: <code v-pre>[example]:this is an example template!</code> <i>(The trigger word being <code v-pre>[example]</code> and the message being <code v-pre>this is an example template!</code>. (YOU NEED THE <code v-pre>:</code>!))</i> You can add as many of these as you want. You can then save the file, boot up the mod, and run <code v-pre>/t [example]</code> in your chat box to see if it's set up to your liking!</p>
<p>Alternatively, you may also use this <a href="https://ultradragon005.github.io/AmongUs-Utilities/editor.html" target="_blank" rel="noopener noreferrer">Template Editor<ExternalLinkIcon/></a> put together by one of TOHE's Contributors, <a href="https://github.com/Ultradragon005" target="_blank" rel="noopener noreferrer">Drakos<ExternalLinkIcon/></a>.</p>
