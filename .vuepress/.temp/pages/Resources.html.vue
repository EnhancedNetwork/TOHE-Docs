<summary><b><font color=gray>template.dat Info</font></b></summary>
<p>You can edit your <code v-pre>template.dat</code> to say really anything you want! You can also download custom templates shared by others. If you want to use one, make sure the file is renamed to <code v-pre>english.dat</code> before using it, if it isn't already. (NOTE: english.dat is only if you are on the English Translation of base game Among Us. If you are using another Translation, please rename the english part to the translation you are using. (ie: <code v-pre>Spanish.dat/SChinese.dat/Latam.dat</code>/so on.) A list of all base game supported translations can be found on the <RouteLink to="/Resources.html">Resources</RouteLink> Page.)<br><br></p>
<p>Note: You have to put the file in the following directory: <code v-pre>.\Among Us\Language</code> for it to work. (Make sure your file is a <code v-pre>.dat</code> file, otherwise it will not work. Paste it alongside the <code v-pre>template.dat</code> file already in your folder.)<br><br></p>
<p>You can do a lot with your <code v-pre>template.dat</code>, for this though, we'll refer to it as <code v-pre>english.dat</code> for simplicity sake. For starters, you can edit the Host Text, Icon, &amp; Color. I won't go into depth on <i>everything</i> that the <code v-pre>english.dat</code> can edit, but it's almost every line of text that can be displayed on the screen. Once you get the hang of editing the Host Text, Icon, &amp; Color, you can move down the list to edit anything you really want to (Such as Role Names, Role Descriptions, VIP Text, Moderator Text, and more).<br>
Below is what the default <code v-pre>template.dat</code> looks like, Versus what the edited <code v-pre>english.dat</code> I have looks like.<br>
