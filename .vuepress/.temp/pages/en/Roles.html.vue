<h2 id="⭐-impostor" tabindex="-1"><a class="header-anchor" href="#⭐-impostor" aria-hidden="true">#</a> ⭐ Impostor</h2>
<h3 id="赏金猎人" tabindex="-1"><a class="header-anchor" href="#赏金猎人" aria-hidden="true">#</a> <font color=red>赏金猎人</font></h3>
<p>赏金猎人会被随机分配一个悬赏目标，若在规定时间内击杀了悬赏目标，则击杀CD会变得很短，能立刻进行下一次击杀。若赏金猎人击杀的人不是赏金目标，则击杀CD会被延长。若赏金猎人在规定时间内没有完成任何击杀，则目标会被随机切换为另一个人。</p>
<blockquote>
<p>来源：TOH</p>
</blockquote>
<hr>
<h3 id="嗜血杀手" tabindex="-1"><a class="header-anchor" href="#嗜血杀手" aria-hidden="true">#</a> <font color=red>嗜血杀手</font></h3>
<p>嗜血杀手的击杀CD很短，但是如果在限定时间内没有进行击杀则会自杀。</p>
<blockquote>
<p>来源：TOH</p>
</blockquote>
<hr>
<h3 id="吸血鬼" tabindex="-1"><a class="header-anchor" href="#吸血鬼" aria-hidden="true">#</a> <font color=red>吸血鬼</font></h3>
<p>吸血鬼可以通过击杀键对目标进行吸血，被吸血的目标不会立刻死亡也不会知道自己被吸血。延迟一段时间后，目标会原地死亡。</p>
<blockquote>
<p>来源：TOH</p>
</blockquote>
<hr>
<h3 id="术士" tabindex="-1"><a class="header-anchor" href="#术士" aria-hidden="true">#</a> <font color=red>术士</font></h3>
<p>术士可以通过击杀键对目标下咒，若下咒成功，术士自己身上会出现护盾破碎的动画作为提示（只有术士自己能看到）。被下咒的目标不会知道自己被下咒。术士在完成下咒后可以随时使用变形键操控被诅咒的目标去击杀距离自己最近的玩家。（对摆烂人下咒无效）</p>
<blockquote>
<p>来源：TOH</p>
</blockquote>
<hr>
<h3 id="刺客" tabindex="-1"><a class="header-anchor" href="#刺客" aria-hidden="true">#</a> <font color=red>刺客</font></h3>
<p>刺客可以使用击杀键标记一个目标，标记成功则自己身上会出现碎盾动画作为提示。之后刺客可以使用变形传送至被标记者的身边并击杀被标记者。刺客不可以进行常规击杀，但根据房主设定可能可以在刺杀后进行常规击杀。</p>
<blockquote>
<p>来源：TOHR &amp; 程序设计：<a href="https://github.com/KARPED1EM" target="_blank" rel="noopener noreferrer">咔皮呆<ExternalLinkIcon/></a></p>
</blockquote>
<hr>
<h3 id="僵尸" tabindex="-1"><a class="header-anchor" href="#僵尸" aria-hidden="true">#</a> <font color=red>僵尸</font></h3>
<p>僵尸的击杀冷却很短，但移动速度很慢且视野极小。僵尸不会被独裁者以外的人票出，僵尸的移动速度会随着击杀或时间流逝逐渐变慢。</p>
<blockquote>
<p>来源：<a href="https://github.com/KARPED1EM" target="_blank" rel="noopener noreferrer">咔皮呆<ExternalLinkIcon/></a> 原创 &amp; 程序设计：<a href="https://github.com/KARPED1EM" target="_blank" rel="noopener noreferrer">咔皮呆<ExternalLinkIcon/></a></p>
</blockquote>
<hr>
<h3 id="骇客" tabindex="-1"><a class="header-anchor" href="#骇客" aria-hidden="true">#</a> <font color=red>骇客</font></h3>
<p>骇客可以使用变形骇入变形的目标，被骇入的目标会立刻报告骇客本轮击杀的尸体，若本轮骇客没有击杀，则随机报告场上存在的尸体，若场上不存在尸体则目标报告自己的尸体（效果类似被害妄想症）。请注意：骇客技能无视尸体是否可以被报告，此外，摆烂人不会替你办事，骇入摆烂人只会浪费你的时间。</p>
<blockquote>
<p>来源：<a href="https://github.com/KARPED1EM" target="_blank" rel="noopener noreferrer">咔皮呆<ExternalLinkIcon/></a> 原创 &amp; 程序设计：<a href="https://github.com/KARPED1EM" target="_blank" rel="noopener noreferrer">咔皮呆<ExternalLinkIcon/></a></p>
</blockquote>
<hr>
<h3 id="矿工" tabindex="-1"><a class="header-anchor" href="#矿工" aria-hidden="true">#</a> <font color=red>矿工</font></h3>
<p>矿工使用变形以及解除变形的时候，会传送至上一个自己使用过的通风管道。若没有上一个使用过的通风管道，则技能不会生效。</p>
<blockquote>
<p>来源：TOHR &amp; 程序设计：<a href="https://github.com/KARPED1EM" target="_blank" rel="noopener noreferrer">咔皮呆<ExternalLinkIcon/></a></p>
</blockquote>
