<summary><b><font color=gray>TOHE-DATA Info</font></b></summary>
<p>Open the root directory of game: <code v-pre>...\Among Us\TOHE-DATA\</code></p>
<p>There are a few files:</p>
<ul>
<li>
<p><code v-pre>BanList.txt</code>: A list of friendcodes that will be unable to join your lobby.</p>
</li>
<li>
<p><code v-pre>Default_Teamplate.txt</code>: This is the Default TOHE Template file. If you wish to reset your templates to default, You can use this. (No idea why it says Teamplate.)</p>
</li>
<li>
<p><code v-pre>DenyName.txt</code>: A list of names that will be filtered out of the game.</p>
</li>
<li>
<p><code v-pre>Moderators.txt</code>: A list of friendcodes that will receive a nice Moderator tag (editable via <code v-pre>...\Among Us\Language\english.dat</code>) as well as permissions to moderate your lobby (such as using the commands near the top of the page). Only grant users you trust these permissions! (NOTE: <code v-pre>english.dat</code> is only if you are on the English Translation of base game Among Us. If you are using another Translation, please rename the english part to the translation you are using. (ie: <code v-pre>Spanish.dat/SChinese.dat/Latam.dat</code>/so on.) A list of all base game supported translations can be found on the <RouteLink to="/Resources.html">Resources</RouteLink> Page.)</p>
</li>
<li>
<p><code v-pre>template.txt</code>: You can modify the <code v-pre>welcome</code> and <code v-pre>onMeeting</code> messages here. You can also add custom templates here as well following the same format as <code v-pre>welcome</code> &amp; <code v-pre>onMeeting</code> use.</p>
</li>
<li>
<p><code v-pre>VIP-List.txt</code>: A list of friendcodes that will be given a sweet VIP tag (editable via <code v-pre>...\Among Us\Language\english.dat</code>) as well as permissions to change their name color. (NOTE: <code v-pre>english.dat</code> is only if you are on the English Translation of base game Among Us. If you are using another Translation, please rename the english part to the translation you are using. (ie: <code v-pre>Spanish.dat/SChinese.dat/Latam.dat</code>/so on.) A list of all base game supported translations can be found on the <RouteLink to="/Resources.html">Resources</RouteLink> Page.)</p>
</li>
<li>
<p><code v-pre>WhiteList.txt</code>: A list of friendcodes that will be exempt from blacklisted platforms, along with level requirements.</p>
</li>
</ul>
