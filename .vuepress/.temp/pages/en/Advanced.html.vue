<p>there are four files:</p>
<ul>
<li>BanList.txt  -  baned players（friendcode）</li>
<li>BanWords.txt  -  baned words</li>
<li>DenyName.txt  -  baned name</li>
<li>template.txt  -  message template（[above](#Welcome &amp; onMeeting auto messages)）</li>
